    def get_reward(self, t, state_old, state):
        """
        t: 一个episode中时间步长
        state_old: 上时刻的状态(1*(1+64*64+28)的list)
        return: 奖励Reward
        """
        reward = 0
        target_x = 0.11
        target_y = 0.21
        target_z = 0.034
        t_sum = 3500
        tau_1, tau_2, tau_3 = 100, 0.05, 1.0
        r_u = -10  # 惩罚因子
        r_g = 100  # 完成任务奖励因子
        ag = -0.9
        tau_4 = 1 + ag * (t / t_sum)

        if state[0] == 1:  # 触发碰撞惩罚
            reward += tau_3 * r_u
        x, y, z = state[64 * 64 + 3], state[64 * 64 + 4], state[64 * 64 + 5]
        if y <= -0.62:  # 被认定为机器人没有进行爬楼梯，进行惩罚纠正
            reward += tau_3 * r_u
        x_old, y_old, z_old = state_old[64 * 64 + 3], state_old[64 * 64 + 4], state_old[64 * 64 + 5]
        D1 = math.sqrt((x_old - target_x) ** 2 + (y_old - target_y) ** 2 + (z - target_z) ** 2)
        D2 = math.sqrt((x - target_x) ** 2 + (y - target_y) ** 2 + (z - target_z) ** 2)
        if D2 <= 0.045:  # 任务完成触发奖励
            reward += tau_4 * r_g
        deta_d = D1 - D2
        v_x, v_y, v_z = self.robot.getVelocity()[0], self.robot.getVelocity()[1], self.robot.getVelocity()[2]
        d_x, d_y, d_z = target_x - x, target_y - y, target_z - z
        cos_a = (v_x * d_x + v_y * d_y + v_z * d_z)/(math.sqrt(v_x**2+v_y**2+v_z**2) * math.sqrt(d_x**2+d_y**2+d_z**2))
        reward += tau_1 * deta_d + tau_2 * cos_a

        return reward

    def is_done(self, state):
        done = 0
        x, y, z = self.robot.getPosition()
        target_x, target_y, target_z = 0.11, 0.21, 0.034
        # 机器人到达目标区域
        if math.sqrt((x - target_x) ** 2 + (y - target_y) ** 2 + (z - target_z) ** 2) <= 0.045:
            done = 1
        # 机器人发生碰撞
        if state[0] == 1:
            done = 1
        # 机器人远离目标
        if y <= -0.62:
            done = 1

        return done

    def solved(self):
        x, y, z = self.robot.getPosition()
        target_x, target_y, target_z = 0.11, 0.21, 0.034
        if math.sqrt((x - target_x) ** 2 + (y - target_y) ** 2 + (z - target_z) ** 2) <= 0.045:
            return True

